export function SocialShareButtons({
  payload,
  variant = "rays",
  accent,
  label = "Share",
  className = "",
  hideNativeWhenUnavailable = true,
}: Props) {
  const [status, setStatus] = useState<string | null>(null);
  const rays = variant === "rays";
  const compact = variant === "compact";
  const color = accent ?? (rays ? "#C9A227" : "#1E4A6E");

  const flash = useCallback((message: string) => {
    setStatus(message);
    window.setTimeout(() => setStatus(null), 2800);
  }, []);

  const platforms = SOCIAL_PLATFORMS.filter(
    (p) => p.id !== "native" || !hideNativeWhenUnavailable || canUseNativeShare()
  );

  const handleClick = async (platform: SocialPlatform) => {
    if (!payload.url) {
      flash("Link not ready yet");
      return;
    }

    if (platform === "native") {
      const ok = await triggerNativeShare(payload);
      if (!ok) flash("Sharing not available on this device");
      return;
    }

    if (platform === "copy") {
      const ok = await copyShareText(payload.url);
      flash(ok ? "Link copied" : "Could not copy — copy from the address bar");
      return;
    }

    if (platform === "instagram") {
      const ok = await copyShareText(buildInstagramCaption(payload));
      flash(ok ? "Caption copied — paste in Instagram" : "Could not copy caption");
      return;
    }

    const shareUrl = getShareUrl(platform, payload);
    if (!shareUrl) return;

    if (platform === "email") {
      window.location.href = shareUrl;
      return;
    }

    openShareWindow(shareUrl);
  };

  const btnClass = compact
    ? "flex h-9 w-9 items-center justify-center rounded-full border transition hover:opacity-90"
    : "flex h-10 w-10 items-center justify-center rounded-full border-2 transition hover:opacity-90";

  return (
    <div className={className}>
      {label ? (
        <p
          className={
            compact
              ? "text-xs font-semibold uppercase tracking-wider text-neutral-500"
              : rays
                ? "text-xs font-bold uppercase tracking-widest text-rays-gray"
                : "text-sm font-semibold text-neutral-700"
          }
        >
          {label}
        </p>
      ) : null}
      <div
        className={`flex flex-wrap gap-2 ${label ? (compact ? "mt-2" : "mt-3") : ""}`}
        role="group"
        aria-label={label || "Share on social media"}
      >
        {platforms.map((p) => (
          <button
            key={p.id}
            type="button"
            onClick={() => void handleClick(p.id)}
            className={btnClass}
            style={
              rays
                ? { borderColor: color, color }
                : { borderColor: color, color }
            }
            title={p.label}
            aria-label={`Share on ${p.label}`}
          >
            <Icon platform={p.id} />
          </button>
        ))}
      </div>
      {status ? (
        <p className="mt-2 text-xs text-neutral-500" role="status">
          {status}
        </p>
      ) : null}
    </div>
  );
}
